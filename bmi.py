# รับค่าน้ำหนักจากผู้ใช้ (กิโลกรัม)
weight = float(input("กรุณากรอกน้ำหนัก (กก.): "))

# รับค่าส่วนสูงจากผู้ใช้ (เซนติเมตร)
height_cm = float(input("กรุณากรอกส่วนสูง (ซม.): "))

# แปลงส่วนสูงจากเซนติเมตรเป็นเมตร
height_m = height_cm / 100

# คำนวณค่า BMI
bmi = weight / (height_m ** 2)

# แสดงค่า BMI ทศนิยม 2 ตำแหน่ง
print(f"\nค่า BMI ของคุณคือ: {bmi:.2f}")

# แปลผลสุขภาพตามค่า BMI
if bmi < 18.5:
    print("ผลลัพธ์: น้ำหนักน้อย / ผอม")
elif bmi < 23:
    print("ผลลัพธ์: น้ำหนักปกติ")
elif bmi < 25:
    print("ผลลัพธ์: น้ำหนักเกิน")
elif bmi < 30:
    print("ผลลัพธ์: โรคอ้วนระดับ 1")
else:
    print("ผลลัพธ์: โรคอ้วนระดับ 2")